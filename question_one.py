# The volume of a sphere with radius r is (4/3)* pie* r**2
# a) What is the volume of the sphere with radius 5
# Make sure the program enters the radius from the keyboard and provide the answer in two decimal places.
# SOLUTION
radius = int(input("Enter the radius, r: "))
pi = 3.14
volume = (4/3)* pi * (radius ** 2)
print(f"The volume of the sphere is {volume:.2f}")

